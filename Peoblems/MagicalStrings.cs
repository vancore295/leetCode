using System;
using System.Collections.Generic;
using System.Text;

namespace Peoblems
{
    public class MagicalStrings
    {
        public int MagicalString(int n)
        {
            int count = 0;

            for(int i = 0;i < n;i++)
            {
                string s = "";
                if(i == 0)
                {
                    s += "1";
                } 
            }

            return count;
        }
    }
